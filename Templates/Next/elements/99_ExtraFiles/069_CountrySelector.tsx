const AptugoCountrySelector: FunctionComponent<any> = (props) => {
  const selectedCountry = countries.find(country => country.value === props.value) || null

  return (
    <FormControl
      margin={props.margin} 
      size={props.size}
      variant="standard"
      fullWidth
    > {}
      <Autocomplete
        options={countries}
        getOptionLabel={(option) => option.label}
        isOptionEqualToValue={(option, value) => option.value === value.value}
        value={selectedCountry}
        onChange={(event, newValue) => {
          props.onChange({ target: { value: newValue ? newValue.value : '' } })
        }}
        renderInput={(params) => (
          <TextField
            {...params}
            label={props.label}
            variant={props.variant}
            inputProps={ {
              ...params.inputProps,
              autoComplete: 'new-password',
            } }
          />
        )}
        filterOptions={(options, { inputValue }) => {
          const lowerCaseInputValue = inputValue.toLowerCase()
          return options.filter((country) =>
            country.value.toLowerCase().includes(lowerCaseInputValue)
          )
        }}
        renderOption={(props, option) => (
            <MenuItem {...props}>
                {option.label}
            </MenuItem>
        )}
      />
    </FormControl>
  )
}

export default AptugoCountrySelector